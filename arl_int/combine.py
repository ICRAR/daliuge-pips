from arl.visibility.operations import combine_visibility

from .common import load, dump


def main():
    vis1 = load(1)
    vis2 = load(2)
    visres = combine_visibility(vis1, vis2, 1.0, -1.0)
    dump(3, visres)

if __name__ == '__main__':
    main()